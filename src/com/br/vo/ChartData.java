package com.br.vo;

public class ChartData {
	
	private String seq;
	private int icecream;
	private int cake;
	private int total;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public int getIcecream() {
		return icecream;
	}
	public void setIcecream(int icecream) {
		this.icecream = icecream;
	}
	public int getCake() {
		return cake;
	}
	public void setCake(int cake) {
		this.cake = cake;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
}
